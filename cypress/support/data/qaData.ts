import { jsonString, stringToFloat, stringToInt } from 'lib/utils'
import { OasysDateTime } from 'oasys'

export class QaData {

    constructor(qa: string[][]) {

        qa.forEach((q) => {

            // select oq.ref_question_code, oq.free_format_answer, oq.additional_note, ra.ref_section_answer, osec.ref_section_code

            // Some questions require the section name added for uniqueness
            const question = addSectionName.includes(q[0]) ? `${q[4]}-${q[0]}` : q[0]

            switch (qaDetails[question]) {
                case undefined:
                case null:
                    this[question] = null
                    break
                case 'date':
                    const date = OasysDateTime.stringToDate(q[1])
                    this[question] = date == null ? '' : date.toString()
                    break
                case 'text':
                    this[question] = q[1]
                    break
                case 'textarea':
                    this[question] = jsonString(q[2], {remove002: true, removeFf: true})
                    break
                case 'integer':
                    this[question] = stringToInt(q[1])
                    break
                case 'float':
                    this[question] = stringToFloat(q[1])
                    break
                case 'checkbox':
                    if (q[3] != null) {
                        if (this[question] == undefined) {
                            this[question] = []
                        }
                        this[question].push(q[3])
                    }
                    break
                case 'singleCheckboxYes':
                    this[question] = q[3] ? 'Yes' : null
                    break
                default:
                    this[question] = q[3]
            }

        })
    }

    getNumber(key: string): number {

        return this[key] ?? null
    }

    getString(key: string, dictionary: {} = null): string {

        if (this[key] == undefined) {
            return qaDetails[key] == 'date' ? '' : null
        }
        return dictionary == null ? this[key] : dictionary[this[key]]
    }

    getStringArray(key: string): string[] {

        const result = this[key] ?? []
        return result.length == 0 ? null : result
    }

    getOasysScore(key: string): number {

        if (this[key] == null) {
            return null
        }

        const c = this[key].substring(0, 1)
        return Number.isNaN(Number.parseInt(c)) ? null : Number.parseInt(c)
    }

    getMultipleAsArray(keys: string[], dictionary: {}): string[] {

        const result = []
        keys.forEach((key) => {
            const value = this.getString(key)
            if (value != null && value != 'No') {
                result.push(dictionary[key])
            }
        })
        return result.length == 0 ? null : result
    }

}

